package com.bootcoding.java.basic;

public class ForLoopex4 {
    public static void main(String args[]) {
        System.out.println("using For loop");
        //nested for loop
        for (int i = 0; i <= 0; i++) {
            for (int k = 0; k <= i; k++ ){
                System.out.println("*");
            }
            System.out.print("  ");
        }
    }
}
  
	  